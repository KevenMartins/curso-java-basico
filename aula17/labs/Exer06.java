package com.keven.cursojava.aula17.labs;

public class Exer06 {

	public static void main(String[] args) {
		
		
		int count = 1;
		
		while(count <= 20) {
			System.out.println(count);
			count += 1;
		}
		
		count = 1;
		while(count <= 20) {
			System.out.print(count + ", ");
			count += 1;
		}

	}

}
